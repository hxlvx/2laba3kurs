import numpy as np
# 1файл для пул реквесту
# 1. Створення двовимірного масиву 15x15 з числами від 1 до 225
array_2d = np.arange(1, 226).reshape(15, 15)
print("Двовимірний масив 15x15 (перші 3 рядки):")
print(array_2d[:3, :])

# 2. Обчислення суми по кожному стовпцю
column_sums = np.sum(array_2d, axis=0)
print("\nСума по кожному стовпцю:")
print(column_sums)

# 3. Знаходження стандартного відхилення елементів масиву
std_deviation = np.std(array_2d)
print("\nСтандартне відхилення елементів масиву:", std_deviation)

# 4. Транспонування масиву
transposed_array = array_2d.T
print("\nТранспонований масив (перші 3 рядки):")
print(transposed_array[:3, :])

# Додаткова перевірка транспонування
print("\nПеревірка: елемент [0,5] початкового масиву:", array_2d[0, 5])
print("Перевірка: елемент [5,0] транспонованого масиву:", transposed_array[5, 0])